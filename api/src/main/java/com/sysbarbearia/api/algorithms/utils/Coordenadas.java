package com.sysbarbearia.api.algorithms.utils;

public class Coordenadas {
    private Double x;
    private Double y;

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Coordenadas(Double x, Double y) {
        this.x = x;
        this.y = y;
    }
}
